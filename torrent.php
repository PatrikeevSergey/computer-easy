    <b>Терминология</b>
    <p class="tit">Торрент-трекер - сайт, на котором имеется информация о выложенных для скачивания файлах. На некоторых торрент-трекерах регистрация не нужна, просто выбираете нужный Вам файл, щелкаете по нему и качаете. На таких трекерах никого не волнует сколько вы скачали файлов и сколько раздали. Пример такого трекера - <a href="http://www.torrentino.com/">  torrentino.com</a>. Регистрации нет, зато очень много рекламы, да еще и минуту придется ждать, чтобы скачать торрент-файл. Есть другие торрент-трекеры, на которых, во-первых, нужна регистрация, во-вторых, следят на Вашим рейтингом - отношение между скаченными и раздаными файлами. Если много качаете и мало раздаете или вообще не раздаете, то Вас могут наказать каким-нибудь ограничением, либо вообще закрыть вашу регистрацию.</p>
    <p class="tit">Самый известный в России предатвитель такого трекера - <a href="http://rutracker.org/forum/index.php" target="_blank">rutracker.org</a>. Там требуется регистрация - вводится логин, пароль и адрес электронной почты, на который приходит ссылка на подтверждение регистрации. </p>
    <p class="tit">Сид - пользователь, который полностью скачал файл, или файлы, целиком и продолжает его (их) раздавать. Так же указывается максимальная скорость отдачи. Если у файла отсутствует сиды, то это вовсе не значит, что Вы не сможете скачать файл целиком. </p>
    <p class="tit">Пир - пользователь, который участвует в раздаче в данный момет. </p>
    <p class="tit">Лич - пользователь,который скачать часть файла и раздает его.</p>
    <p class="tit">Скачав торрент файл, открываете его в торрент - клиенте. Речь идет о &#956;Torrent, он выдаст диалоговое окно, в котором нужно будет указать куда поместить файл. В окне можно посмотреть всю информацию по загружаемому файлу. </p>
