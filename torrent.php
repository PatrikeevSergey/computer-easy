  <p class="tit">Многим из Вас, наверно, приходилось слышать такие фразы "Скачать из торрента", "Посмотри, в торренте должно быть". А что такое торрент и что там есть, такие вопросы очень даже часто волнуют пользователей, которые никогда не сталкивались с чем-то подобным. Давайте разберемся что такое торрент и как им пользоваться.</p>
  <p class="tit">Торрент (BitTorrent) - специальный протокол для обмена файлами между пользователей. Основным отличием этого протокола является то, что пользователь не скачивает файл с какого-то сервара, а у другого пользователя. Проще говоря, пользователи копируют файлы друг у друга.</p>
  <p class="tit">Сама технология обмена организуется очень хитрым способом: протокол выбирает наиболее быстрый источник, если с ним что-то происходит (прерывается раздача, падает скорость) - протокол выбирает другой источник. Кроме этого, файлы качаются разными сигментами, это позволяет гибко организовать раздачу, как только Вы начинаете качать файл, одновременно подключаетесь к раздаче, это позволяет другим пользователям скачать другим пользователям скачать ту часть файла, которую Вы уже скачали.</p>
  <p class="tit">Согласен, система очень замудренная, но всю "головную боль" берет на себя BitTorrent-клиент.</p>
