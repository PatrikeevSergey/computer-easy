  <p class="tit">Основной момент, который нужно понимать - на сервере, где организуется раздача разных файлов (их еще называют торрент-трекеры), физическки не лежит ни один файл. Все файлы находятся у пользователей. Задача сервера - предоставлять список выложенных файлов и координировать раздачу. Однако, бывают случаи, когда файл есть в списке, но его не получается скачать, его просто никто из пользователей не раздает. Ну при таком количестве народа, которое пользуется торрентом, подобные случаи бывают очень редко. Кроме того, нередко бывает, что раздачи никакой нет, а позже, ночью например, кто-то из пользователей подключился и спокойно качаете файл. Еще один не маловажный момент при работе с программой - трекером - все проходит в теневом режиме и не требует больших ресурсов компьютера. Т.е. Вы выбрали нужные файлы, поставили загрузку и занимаетесь своими делами, программа "сидит" в системной области уведомлений и качает по очереди файлы. Поэтому торрент - клиент удобен для обмена файлами в Интернете.</p>
